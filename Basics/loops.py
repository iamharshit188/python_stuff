# Iteration in python
# for loop

print("----For loop ----")
for i in range(5):
    print(i)
# we can also pass _ instead of i if we don't want to use the variable
for _ in range(5):
    print("Hello, World!")
 
print("----While loop ----")
count = 0 
while count < 5 :
    print(count)
    count +=1

print("---- Looping over string -----")
text = "God is a Woman"
for words in text:
    print(words)
    
